#!/usr/bin/env bash
# test.sh — offline tests for agent-github-access scripts
#
# Runs install.sh for real (with mocked gh and browser) to generate
# authenticate-github.sh, then tests its behaviour and onboard-repo.sh with
# mocked network calls.  No real GitHub API calls are made.
#
# Requirements: bash, python3, openssl, jq, curl, base64
set -uo pipefail

DIR="$(cd "$(dirname "$0")/.." && pwd)"
PASS=0; FAIL=0
ERRORS=()

ok()   { PASS=$((PASS+1)); printf "PASS  %s\n" "$1"; }
fail() { FAIL=$((FAIL+1)); ERRORS+=("$1"); printf "FAIL  %s\n" "$1"; }

# ── Setup ─────────────────────────────────────────────────────────────────────

TMPDIR_T=$(mktemp -d)
trap 'rm -rf "$TMPDIR_T"' EXIT

TEST_APP_ID="999888"
TEST_OWNER="testowner"
TEST_SLUG="${TEST_OWNER}-agent"

# A real RSA key is required — openssl uses it to sign JWTs in the generated script.
TEST_KEY_FILE="$TMPDIR_T/test.pem"
openssl genrsa -out "$TEST_KEY_FILE" 2048 2>/dev/null

# Write the fake /app-manifests/.../conversions response to a file so the mock
# gh can cat it.  python3 handles JSON-escaping the PEM (newlines etc).
CONVERSION_JSON="$TMPDIR_T/conversion.json"
python3 - "$TEST_KEY_FILE" "$TEST_APP_ID" "$TEST_SLUG" "$CONVERSION_JSON" << 'PYEOF'
import json, sys
key_file, app_id, slug, out = sys.argv[1:]
pem = open(key_file).read()
with open(out, 'w') as f:
    json.dump({"id": int(app_id), "slug": slug, "pem": pem}, f)
PYEOF

# ── Mock binaries for install.sh ─────────────────────────────────────

MAIN_BIN="$TMPDIR_T/main-bin"
mkdir -p "$MAIN_BIN"

# gh: handles all calls made by install.sh (which now also runs
# onboard-repo.sh for the fork before app creation).
cat > "$MAIN_BIN/gh" << GHEOF
#!/usr/bin/env bash
args="\$*"
if [[ "\$args" == *"user"* && "\$args" == *".login"* ]]; then
  printf '%s' "${TEST_OWNER}"
elif [[ "\$args" == *"secrets/GH_APP_ID"* && "\$args" != *"--method"* ]]; then
  # Secret existence check — return 404 (not yet set) so app creation proceeds
  exit 1
elif [[ "\$args" == *"secrets/public-key"* ]]; then
  # Return a fake libsodium public key (32 bytes, base64-encoded)
  printf '{"key_id":"1234","key":"hiNFHFRJGnHP5UNKhXkMEEI7iNLMFHmqhAeH3xQBtUE="}'
elif [[ "\$args" == *"secrets/"* && "\$args" == *"--method PUT"* ]]; then
  exit 0
elif [[ "\$args" == *"rulesets"* && "\$args" == *"--method DELETE"* ]]; then
  exit 0
elif [[ "\$args" == *"rulesets"* && "\$args" == *"--method POST"* ]]; then
  printf '{"id":1}'
elif [[ "\$args" == *"rulesets"* ]]; then
  # GET rulesets — return empty list (no pre-existing rulesets)
  printf '[]'
elif [[ "\$args" == *"workflow run"* ]]; then
  exit 0
fi
exit 0
GHEOF
chmod +x "$MAIN_BIN/gh"

# curl: intercept the unauthenticated app-manifests conversion call; pass everything else through.
REAL_CURL=$(command -v curl)
cat > "$MAIN_BIN/curl" << CURLEOF
#!/usr/bin/env bash
if [[ "\$*" == *"app-manifests"* ]]; then
  cat "${CONVERSION_JSON}"
else
  "${REAL_CURL}" "\$@"
fi
CURLEOF
chmod +x "$MAIN_BIN/curl"

# xdg-open: for file:// URLs, simulate the OAuth callback instead of opening a
# browser. The real Python HTTP server is already running and will write the code
# to CODEFILE, unblocking `wait $SERVER_PID` in install.sh.
# For the https:// installation URL opened at the end, do nothing.
cat > "$MAIN_BIN/xdg-open" << 'XDGEOF'
#!/usr/bin/env bash
if [[ "$1" == http://localhost:* ]]; then
  PORT="${1#http://localhost:}"
  PORT="${PORT%%/*}"
  sleep 0.3
  curl -sf "http://localhost:${PORT}/callback?code=testcode123" > /dev/null || true
fi
XDGEOF
chmod +x "$MAIN_BIN/xdg-open"

# ── 1. Syntax: install.sh ────────────────────────────────────────────

bash -n "$DIR/install.sh" 2>/dev/null \
  && ok  "syntax: install.sh" \
  || fail "syntax: install.sh"

# ── 2. Run install.sh to generate authenticate-github.sh ─────────────
# Pass TEST_OWNER as the username arg to skip the multi-account check.
# Run from a work dir so the generated script lands there, not in the repo root.

WORK="$TMPDIR_T/work"
mkdir -p "$WORK"

GEN_OUTPUT=$(cd "$WORK" && printf 'y\ny\n' | PATH="$MAIN_BIN:$PATH" bash "$DIR/install.sh" "$TEST_OWNER" 2>&1) || true

TEST_AUTH="$WORK/authenticate-github.sh"
# onboard-repo.sh is now a static file in the repo (not generated by install.sh)
TEST_ONBOARD="$DIR/onboard-repo.sh"

if [[ -f "$TEST_AUTH" ]]; then
  ok  "generate: authenticate-github.sh produced"
else
  fail "generate: authenticate-github.sh produced"
fi

# ── 3. Syntax: generated and static scripts ────────────────────────────────────

bash -n "$TEST_AUTH" 2>/dev/null \
  && ok  "syntax: authenticate-github.sh" \
  || fail "syntax: authenticate-github.sh"

bash -n "$TEST_ONBOARD" 2>/dev/null \
  && ok  "syntax: onboard-repo.sh" \
  || fail "syntax: onboard-repo.sh"

# Save original onboard-repo.sh so nondefault install runs (which call
# "sed -i" on it) can be restored without breaking later onboard tests.
ONBOARD_ORIG=$(cat "$DIR/onboard-repo.sh")

# ── 3b. Nondefault prompt: actions=n, verified=y ──────────────────────────────
# Answering 'n' to actions:write and 'y' to verified commits.
# The generated authenticate-github.sh should still be produced and contain
# the COMMIT METHOD block (verified=y) but use actions:read in the manifest.

WORK_NO_ACTIONS="$TMPDIR_T/work-no-actions"
mkdir -p "$WORK_NO_ACTIONS"

printf 'n\ny\n' | PATH="$MAIN_BIN:$PATH" bash "$DIR/install.sh" "$TEST_OWNER" \
  > "$TMPDIR_T/gen-no-actions.log" 2>&1 || true

AUTH_NO_ACTIONS="$WORK_NO_ACTIONS/authenticate-github.sh"
# install.sh always writes authenticate-github.sh in the current directory
cp "$WORK/authenticate-github.sh" "$AUTH_NO_ACTIONS" 2>/dev/null || true
# Re-generate in correct workdir
rm -f "$WORK_NO_ACTIONS/authenticate-github.sh"
(cd "$WORK_NO_ACTIONS" && printf 'n\ny\n' | PATH="$MAIN_BIN:$PATH" bash "$DIR/install.sh" "$TEST_OWNER" > /dev/null 2>&1) || true

if [[ -f "$AUTH_NO_ACTIONS" ]]; then
  ok  "generate actions=n: authenticate-github.sh produced"
else
  fail "generate actions=n: authenticate-github.sh produced"
fi

bash -n "$AUTH_NO_ACTIONS" 2>/dev/null \
  && ok  "syntax actions=n: authenticate-github.sh" \
  || fail "syntax actions=n: authenticate-github.sh"

# Restore onboard-repo.sh mutated by the install run above
printf '%s' "$ONBOARD_ORIG" > "$DIR/onboard-repo.sh"

# ── 3c. Nondefault prompt: actions=y, verified=n ──────────────────────────────
# Answering 'y' to actions:write and 'n' to verified commits.
# The generated authenticate-github.sh should omit the COMMIT METHOD block
# and check only one ruleset (no must-sign filter).

WORK_NO_VERIFIED="$TMPDIR_T/work-no-verified"
mkdir -p "$WORK_NO_VERIFIED"
(cd "$WORK_NO_VERIFIED" && printf 'y\nn\n' | PATH="$MAIN_BIN:$PATH" bash "$DIR/install.sh" "$TEST_OWNER" > /dev/null 2>&1) || true

AUTH_NO_VERIFIED="$WORK_NO_VERIFIED/authenticate-github.sh"

if [[ -f "$AUTH_NO_VERIFIED" ]]; then
  ok  "generate verified=n: authenticate-github.sh produced"
else
  fail "generate verified=n: authenticate-github.sh produced"
fi

bash -n "$AUTH_NO_VERIFIED" 2>/dev/null \
  && ok  "syntax verified=n: authenticate-github.sh" \
  || fail "syntax verified=n: authenticate-github.sh"

grep -q "COMMIT METHOD" "$AUTH_NO_VERIFIED" \
  && fail "generate verified=n: commit method block absent" \
  || ok  "generate verified=n: commit method block absent"

# Restore onboard-repo.sh mutated by the verified=n install run above
printf '%s' "$ONBOARD_ORIG" > "$DIR/onboard-repo.sh"

# ── 4. JWT format ─────────────────────────────────────────────────────────────
# Run the JWT construction logic in a subshell using the test key.  No network.

JWT_RESULT=$(bash << BEOF
set -euo pipefail
APP_PEM=\$(cat "$TEST_KEY_FILE")
APP_ID="$TEST_APP_ID"
NOW=\$(date +%s)
EXP=\$((NOW + 540))

b64url() { base64 | tr '+/' '-_' | tr -d '=\n'; }
HEADER=\$(printf '%s' '{"alg":"RS256","typ":"JWT"}' | b64url)
PAYLOAD=\$(printf '{"iat":%d,"exp":%d,"iss":%d}' "\$NOW" "\$EXP" "\$APP_ID" | b64url)

TMPKEY=\$(mktemp)
chmod 600 "\$TMPKEY"
printf '%s' "\$APP_PEM" > "\$TMPKEY"
SIG=\$(printf '%s.%s' "\$HEADER" "\$PAYLOAD" | openssl dgst -binary -sha256 -sign "\$TMPKEY" | b64url)
rm -f "\$TMPKEY"

printf '%s.%s.%s' "\$HEADER" "\$PAYLOAD" "\$SIG"
BEOF
)

if [[ "$JWT_RESULT" =~ ^[A-Za-z0-9_-]+\.[A-Za-z0-9_-]+\.[A-Za-z0-9_-]+$ ]]; then
  ok  "jwt: three-part base64url format"
else
  fail "jwt: three-part base64url format (got: ${JWT_RESULT:0:40}…)"
fi

JWT_HEADER_PART="${JWT_RESULT%%.*}"
PADDED="${JWT_HEADER_PART}==="
DECODED=$(printf '%s' "$PADDED" | tr '_-' '/+' | base64 -d 2>/dev/null || \
          printf '%s' "$PADDED" | tr '_-' '/+' | base64 -D 2>/dev/null)
if printf '%s' "$DECODED" | grep -q '"RS256"'; then
  ok  "jwt: header contains RS256"
else
  fail "jwt: header contains RS256 (got: $DECODED)"
fi

# ── Mock bin for authenticate-github.sh tests (curl-based) ────────────────────

MOCK_BIN="$TMPDIR_T/bin"
mkdir -p "$MOCK_BIN"

# ── 5. Error: no installations ────────────────────────────────────────────────

cat > "$MOCK_BIN/curl" << 'EOF'
#!/usr/bin/env bash
printf '[]'
exit 0
EOF
chmod +x "$MOCK_BIN/curl"

NO_INST_OUTPUT=$(PATH="$MOCK_BIN:$PATH" bash "$TEST_AUTH" 2>&1 || true)
NO_INST_EXIT=$(PATH="$MOCK_BIN:$PATH" bash "$TEST_AUTH" > /dev/null 2>&1; echo $?)

[[ "$NO_INST_EXIT" != "0" ]] \
  && ok  "no-installations: exits non-zero" \
  || fail "no-installations: exits non-zero"

printf '%s' "$NO_INST_OUTPUT" | grep -q "no repositories configured" \
  && ok  "no-installations: error mentions 'no repositories configured'" \
  || fail "no-installations: error mentions 'no repositories configured'"

printf '%s' "$NO_INST_OUTPUT" | grep -q "trusted machine" \
  && ok  "no-installations: error mentions 'trusted machine'" \
  || fail "no-installations: error mentions 'trusted machine'"

# ── 6. Error: no protected repos ──────────────────────────────────────────────

cat > "$MOCK_BIN/curl" << MOCKEOF
#!/usr/bin/env bash
if [[ "\$*" == *"/app/installations" ]]; then
  printf '[{"id":42,"account":{"login":"${TEST_OWNER}","type":"User"}}]'
elif [[ "\$*" == *"/access_tokens" ]]; then
  printf '{"token":"ghs_faketoken123"}'
elif [[ "\$*" == *"/installation/repositories" ]]; then
  printf '{"repositories":[{"id":101,"full_name":"${TEST_OWNER}/testrepo"}]}'
elif [[ "\$*" == */rulesets ]]; then
  printf '[]'
else
  printf '{}'
fi
exit 0
MOCKEOF
chmod +x "$MOCK_BIN/curl"

NO_RULES_OUTPUT=$(PATH="$MOCK_BIN:$PATH" bash "$TEST_AUTH" 2>&1 || true)
NO_RULES_EXIT=$(PATH="$MOCK_BIN:$PATH" bash "$TEST_AUTH" > /dev/null 2>&1; echo $?)

[[ "$NO_RULES_EXIT" != "0" ]] \
  && ok  "no-protected-repos: exits non-zero" \
  || fail "no-protected-repos: exits non-zero"

printf '%s' "$NO_RULES_OUTPUT" | grep -qi "onboard-repo" \
  && ok  "no-protected-repos: error mentions onboard-repo" \
  || fail "no-protected-repos: error mentions onboard-repo"

# ── 7. Output content: global memory bookends and branch prefix ───────────────

cat > "$MOCK_BIN/curl" << MOCKEOF
