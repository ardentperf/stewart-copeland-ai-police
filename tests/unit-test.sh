# Helper: build a curl mock for inventory tests.
# $1 = path to write mock
# $2 = existing inventory content to return (empty = file not found → error)
# $3 = "branch-exists" | "" — kept for call-site compatibility but no longer used
#      (branch init is now done by install.sh, not inventory.sh)
write_inv_curl() {
  local mock_path="$1" existing_inv="$2"
  local existing_b64="" existing_sha="existingfilesha"
  if [[ -n "$existing_inv" ]]; then
    existing_b64=$(printf '%s' "$existing_inv" | base64 | tr -d '\n')
  fi
  cat > "$mock_path" << MOCKEOF
#!/usr/bin/env bash
args="\$*"
if [[ "\$args" == *"/app/installations"* && "\$args" != *"access_tokens"* ]]; then
  printf '[{"id":42}]'
elif [[ "\$args" == *"access_tokens"* ]]; then
  printf '{"token":"ghs_mock"}'
elif [[ "\$args" == *"/installation/repositories"* ]]; then
  printf '{"repositories":[{"full_name":"${TEST_OWNER}/repo1","owner":{"login":"${TEST_OWNER}"}},{"full_name":"${TEST_OWNER}/repo2","owner":{"login":"${TEST_OWNER}"}}]}'
elif [[ "\$args" == *"/rulesets"* ]]; then
  printf '[{"name":"agent-gh-access-apps-blocked-from-non-ai-branches"},{"name":"agent-gh-access-apps-must-sign"}]'
elif [[ "\$args" == *"contents/README"* && "\$args" != *"-X PUT"* ]]; then
  # Inventory fetch: return existing content with SHA, or empty (file not found)
  if [[ -n "${existing_b64}" ]]; then
    printf '{"content":"%s","sha":"${existing_sha}"}' "${existing_b64}"
  else
    printf '{}'
  fi
elif [[ "\$args" == *"-X PUT"* && "\$args" == *"contents/README"* ]]; then
  # Contents API write — capture the -d payload for inspection
  for i in "\$@"; do
    if [[ "\$i" == "{"* ]]; then
      printf '%s' "\$i" >> "${INV_CONTENTS_LOG}"
      printf '\n' >> "${INV_CONTENTS_LOG}"
    fi
  done
  printf '{"content":{"sha":"newfilesha"},"commit":{"sha":"newcommitsha"}}'
fi
MOCKEOF
  chmod +x "$mock_path"
}

